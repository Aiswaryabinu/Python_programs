# Area of rectangle
print("-----------------AREA OF RECTANGLE ------------------")
a = int(input("Enter the length     :"))
b = int(input("Enter the breadth    :"))
area_1 = a*b
print ("Area of rectangle    :"+str(area_1))

#Area of cylinder
print("-----------------AREA OF CYLINDER ------------------")
r = float(input("Enter the raduis   :"))
